#ifndef PRODUCTENUM_H
#define PRODUCTENUM_H

/** enum
 * product
 *      An enum stating which of the four products be sold
 *      is a given product. The values include BASIC for
 *      the basic maintance plan, UPGRADE for the upgrade
 *      maintance plan, DELUXE for the deluxe maintance plan,
 *      and IROBOT for the bomb detecting IRobot itself.
 */
enum product
{
    BASIC,
    UPGRADE,
    DELUXE,
    IROBOT
};

#endif // PRODUCTENUM_H
